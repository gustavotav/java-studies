    private void btnCalcActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalcActionPerformed
        // TODO add your handling code here:
        int v = (Integer) spinnerValor.getValue();

        int res = v % 2;  
        lblRes.setText(Integer.toString(res));
        
        double ele = Math.pow(v, 3);
        lblEle.setText(String.format("%.2f", ele));

        double sqrt = Math.sqrt(v);
        lblSqrt.setText(String.format("%.2f", sqrt));
        
        double cbrt = Math.cbrt(v);
        lblCbrt.setText(String.format("%.2f", cbrt));
        
        double abs = Math.abs(v);
        lblAbs.setText(String.format("%.0f", abs));
    }//GEN-LAST:event_btnCalcActionPerformed

